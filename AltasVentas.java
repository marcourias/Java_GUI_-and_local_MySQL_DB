    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //Crear nueva instancia de la clase DatabaseConnection
    DatabaseConnection dbConn = new DatabaseConnection();
    
    // Jalar los valores del formulario GUI
    String value1 = jTextField1.getText();
    String value2 = jTextField2.getText();
    String value3 = jTextField3.getText();
    String value4 = jTextField4.getText();
    String value5 = jTextField5.getText();
    
    String query = "INSERT INTO ventas (nombre_cliente, precio, fecha, folio, articulo) VALUES (?, ?, ?, ?, ?)";
    
    try {
        //Preparar el statement
        PreparedStatement ps = dbConn.getConnection().prepareStatement(query);
        
        //Asignar los valores a los par√°metros del statement
        ps.setString(1, value1);
        ps.setString(2, value2);
        ps.setString(3, value3);
        ps.setString(4, value4);
        ps.setString(5, value5);
        
        //Ejecutar el statement
        ps.executeUpdate();
        
        //Cerrar el statement
        ps.close();
        
        //Mostrar un cuadro de di√°logo de √©xito
        JOptionPane.showMessageDialog(null, "Datos guardados exitosamente.");
        
        //Limpiar los campos
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
        jTextField5.setText("");
        
    } catch (SQLException ex) {
        ex.printStackTrace();
    } finally {
        //Cerrar la conexi√≥n
        dbConn.closeConnection();
    }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

